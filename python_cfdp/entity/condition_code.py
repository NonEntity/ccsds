from enum import IntEnum

class ConditionCode(IntEnum):
    """Enumeration of CFDP condition codes."""
    CC_NOERROR = 0b0000
    CC_POS_ACK_LIMIT_REACHED = 0b0001
    CC_KEEPALIVE_LIMIT_REACHED = 0b0010
    CC_INVALID_TX_MODE = 0b0011
    CC_FILESTORE_REJECTION = 0b0100
    CC_FILE_CHECKSUM_FAILURE = 0b0101
    CC_FILE_SIZE_ERROR = 0b0110
    CC_NAK_LIMIT_REACHED = 0b0111
    CC_INACTIVITY_DETECTED = 0b1000
    CC_INVALID_FILE_STRUCTURE = 0b1001
    CC_CHECK_LIMIT_REACHED = 0b1010
    CC_UNSUPPORTED_CHECKSUM_TYPE = 0b1011
    CC_SUSPEND_REQUEST_RECEIVED = 0b1110
    CC_CANCEL_REQUEST_RECEIVED = 0b1111
